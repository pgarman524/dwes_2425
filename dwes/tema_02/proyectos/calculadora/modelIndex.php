<?php

$num1;
$num2;
$resultado; 


function suma($num1, $num2){
    $resultado = $num1 + $num2;
    return $resultado;
}
function resta($num1, $num2){
    $resultado = $num1 - $num2;
    return $resultado;
}
function multiplo($num1, $num2){
    $resultado = $num1 * $num2;
    return $resultado;
}
function divisor($num1, $num2){
    $resultado = $num1 / $num2;
    return $resultado;
}

